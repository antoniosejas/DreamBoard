<h2>Funcionamiento</h2>
El diseño de gestión de usuarios de DreamBoard, permite la distinción de 10 diferentes roles. Actualmente se utilizan dos.

<h3>Nivel Usuario</h3>
<h4>Registro</h4>
Si no tienes cuenta de usuario en esta aplicación, puedes navegar como invitado o registrarte para poder crear tus espacios personalizados.
<h4>Login</h4>
Una vez registrado puedes loguearte mediante usuario y password para acceder a tu contenido.
<h4>Crear DreamBoards</h4>
Entendemos como DreamBoard a un conjunto de imágenes donde cada una de ellas representa un sueño u objetivo. Una vez logueado entra en "Add DreamBoard" y crea una nueva.
<h4>Añadir imágenes a tus DreamBoards</h4>
Podrás añadir tantas imágenes como quieras a tus DreamBoards
<h4>Compartir DreamBoards</h4>
Siempre tienes la posibilidad de compartir tus DreamBoard mediante una URL identificativa.
<br />
<h3>Nivel Administrador</h3>
<h4>Editar textos</h4>
Con el Rol de administrador, podrás acceder a las páginas de administración donde podrás editar los textos del resto de las demás páginas.
<h4>Administración Usuarios</h4>
Del mismo modo podrás dar de alta/baja a los usuarios registrados en la plataforma.
